#!/bin/bash

i=0
NODE_DIR[$i]="node_modules/bootstrap/dist";  FILENAME[$i]="bootstrap.min.css"      ;  TARGET_DIR[$i]="src/_libraries/"; let "i += 1"
NODE_DIR[$i]="node_modules/bootstrap/dist";  FILENAME[$i]="bootstrap.bundle.min.js";  TARGET_DIR[$i]="src/_libraries/"; let "i += 1"
NODE_DIR[$i]="node_modules/bootstrap-icons"; FILENAME[$i]="bootstrap-icons.svg"    ;  TARGET_DIR[$i]="src/_images/";    let "i += 1"
NODE_DIR[$i]="node_modules/jquery/dist";     FILENAME[$i]="jquery.min.js"          ;  TARGET_DIR[$i]="src/_libraries/"; let "i += 1"
NODE_DIR[$i]="node_modules/mdbootstrap";     FILENAME[$i]="mdb.min.css"            ;  TARGET_DIR[$i]="src/_libraries/"; let "i += 1"
NODE_DIR[$i]="node_modules/mdbootstrap";     FILENAME[$i]="mdb.min.js"             ;  TARGET_DIR[$i]="src/_libraries/"; let "i += 1"
NODE_DIR[$i]="node_modules/uuid/dist/umd";   FILENAME[$i]="uuidv4.min.js"          ;  TARGET_DIR[$i]="src/_libraries/"; let "i += 1"
NODE_DIR[$i]="node_modules/mustache";        FILENAME[$i]="mustache.min.js"        ;  TARGET_DIR[$i]="src/_libraries/"; let "i += 1"

j=0
while [ $j -lt $i ]; do
  find "${NODE_DIR[$j]}" -name "${FILENAME[$j]}" -exec cp -v '{}' "${TARGET_DIR[$j]}" \; | grep "${FILENAME[$j]}" || exit 1
  let "j += 1"
done
